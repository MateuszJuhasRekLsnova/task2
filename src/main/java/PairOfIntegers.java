public class PairOfIntegers {
    private int smallInt;
    private int bigInt;

    public PairOfIntegers(int smallInt, int bigInt) {
        this.smallInt = smallInt;
        this.bigInt = bigInt;
    }

    public int getSmallInt() {
        return smallInt;
    }

    public void setSmallInt(int smallInt) {
        this.smallInt = smallInt;
    }

    public int getBigInt() {
        return bigInt;
    }

    public void setBigInt(int bigInt) {
        this.bigInt = bigInt;
    }
}
